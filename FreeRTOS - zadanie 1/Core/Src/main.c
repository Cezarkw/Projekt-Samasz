void HAL_TIM_OC_DelayElapsedCallback(TIM_HandleTypeDef *htim)
{
	static uint32_t f_PB8;
	f_PB8=osEventFlagsGet(E_PB8Handle);
	if (f_PB8 == 0x00000001U)
		osSemaphoreRelease(myBinarySem02Handle);
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
	{
		static uint32_t f_PB8;
		static int time=0;
		f_PB8=osEventFlagsGet(E_PB8Handle);
		if (osMessageQueueGetCount(myQueue01Handle)>0)
			osMessageQueueGet(myQueue01Handle, &time,NULL,0);
		if (f_PB8==0x00000000U && GPIO_Pin==GPIO_PIN_0 && time>0)
			{
			osSemaphoreRelease(myBinarySem01Handle);
			__HAL_TIM_SET_COUNTER(&htim3,0);
			__HAL_TIM_SetCompare(&htim3,TIM_CHANNEL_1, time);
			}

	};
